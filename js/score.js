/* =====================================================================
 *  score.js —— 《三体》第一部 三幕配乐
 *  主题动机：霍尔斯特《行星组曲·火星，战争使者》(1914-16, 公有领域)
 *  核心：5/4 拍不祥固定音型 + G 小调半音压迫，象征"来自星空的威胁"。
 *  step = 16 分音符。每小节 5 拍 = 20 steps（5/4）。
 * ===================================================================== */
(function (global) {
  'use strict';
  const HE = global.HE;
  const SCORE = HE.SCORE || {};

  function seq(arr) { return arr.map((a) => ({ s: a[0], n: a[1], len: a[2], gain: a[3] })); }

  /* ============ 第一幕：红岸（孤寂、绝望、一线微光）============
   * G 小调。稀疏的方波长音在高处飘，三角贝斯缓慢下行，
   * 火星固定音型以极弱的力度潜伏（预示），偶发噪声如宇宙背景。
   */
  // 火星 5/4 固定音型（G 踏板，弱起潜伏）——每 20 步一循环
  const MARS = [];
  (function () {
    for (let bar = 0; bar < 8; bar++) {
      const b = bar * 20;
      // 经典节奏： 三连击 + 长 + 短，落在 G
      MARS.push([b + 0, 'G2', 2, 0.14], [b + 2, 'G2', 2, 0.12], [b + 4, 'G2', 2, 0.14],
                [b + 6, 'G2', 4, 0.13], [b + 12, 'G2', 2, 0.12], [b + 14, 'Ab2', 2, 0.12],
                [b + 16, 'G2', 4, 0.14]);
    }
  })();

  const A1_pad = [ // 高处孤悬的长音（红岸的寒夜）
    [0, 'D4', 16, 0.12], [20, 'Eb4', 16, 0.12], [40, 'D4', 20, 0.11],
    [60, 'Bb3', 16, 0.12], [80, 'C4', 16, 0.12], [100, 'D4', 20, 0.12],
    [120, 'Eb4', 16, 0.13], [140, 'D4', 20, 0.12],
  ];
  const A1_lead = [ // 一缕人性的微光（下行叹息动机）
    [40, 'G4', 4, 0.16], [44, 'F4', 4, 0.15], [48, 'Eb4', 6, 0.15], [56, 'D4', 4, 0.14],
    [100, 'G4', 4, 0.16], [104, 'Bb4', 4, 0.16], [108, 'A4', 6, 0.15], [116, 'G4', 4, 0.14],
    [140, 'D4', 4, 0.16], [144, 'Eb4', 4, 0.16], [148, 'D4', 4, 0.15], [152, 'Bb3', 8, 0.14],
  ];
  const A1_bass = [
    [0, 'G1', 20], [20, 'G1', 20], [40, 'Eb1', 20], [60, 'F1', 20],
    [80, 'G1', 20], [100, 'Eb1', 20], [120, 'C2', 20], [140, 'G1', 20],
  ];

  SCORE.act1 = {
    bpm: 60, loop: true, length: 160,
    tracks: [
      { chan: 'pulse', duty: 0.125, gain: 0.13, a: 0.6, d: 0.4, s: 0.6, r: 0.8, legato: 0.98,
        vib: { rate: 3.5, depth: 2.5, delay: 0.5 }, notes: seq(A1_pad) },
      { chan: 'pulse', duty: 0.5, gain: 0.16, a: 0.05, d: 0.2, s: 0.6, r: 0.4, legato: 0.95,
        vib: { rate: 5, depth: 3, delay: 0.2 }, notes: seq(A1_lead) },
      { chan: 'tri', gain: 0.18, a: 0.05, d: 0.2, s: 0.8, r: 0.4, legato: 0.9, notes: seq(MARS) },
      { chan: 'tri', gain: 0.22, a: 0.1, d: 0.3, s: 0.85, r: 0.6, legato: 0.95, notes: seq(A1_bass) },
    ],
  };

  HE.SCORE = SCORE;
  HE._scoreHelpers = { seq, MARS };
})(window);
